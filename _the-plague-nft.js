function set_layer(collection, hash, layer) {

  const categories = collection
    .categories.attributes.filter(
      category => category.hash == hash);

  for (const category of categories) {

    const traits = collection
      .traits.attributes.filter(
        trait => trait.category_id == category.id);

    for (const trait of traits)
      trait.layer = layer;
  }
}

